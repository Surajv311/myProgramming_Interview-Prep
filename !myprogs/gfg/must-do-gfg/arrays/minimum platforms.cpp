	class Solution {
	public:
		//Function to find the minimum number of platforms required at the
		//railway station such that no train waits.
		int findPlatform(int arr[], int dep[], int n)
		{
			// Your code here

			sort(arr, arr + n);
			sort(dep, dep + n);


			int max = 1 ;
			int p = 1 ;

			int i = 1 ; int j = 0;

			while (i < n and j < n) {

				if (arr[i] <= dep[j])
				{
					p++;

					i++;
					if (max < p) {
						max = p ;
					}


				}

				else {

					j++;
					p--;

				}


			}

			return max;

		}
	};




