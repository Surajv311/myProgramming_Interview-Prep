int mcm(int arr[], int i, int j)
{
	if (i >= j) {
		return 0;
	}
	int k;
	int min = INT_MAX;
	int count;

	// placing parenthesis at different places

	for (k = i; k < j; k++)
	{
		count = mcm(arr, i, k) + mcm(arr, k + 1, j) + arr[i - 1] * arr[k] * arr[j];

		if (count < min) {
			min = count;
		}
		// or min = min(min,count);
	}

	return min;
}

// top down dp ~ memoized
int dp[1001][1001];
int mcmm(int arr[], int i, int j)
{
	if (i >= j) {
		return 0;
	}
	int k;
	int min = INT_MAX;
	int count;

	if (dp[i][j] != -1) {
		return dp[i][j];
	}
	for (k = i; k < j; k++)
	{
		count = mcmm(arr, i, k)
		        + mcmm(arr, k + 1, j)
		        + arr[i - 1] * arr[k] * arr[j];

		if (count < min)
			min = count;
	}

	return dp[i][j] = min;
}



