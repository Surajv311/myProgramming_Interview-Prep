	class Solution
	{
	public:
		//Function to reverse words in a given string.
		string reverseWords(string S)
		{
			// code here

			string str = "";
			string temp = "";
			string word = "";

			for (int i = 0 ; S[i] != '\0'; ++i)
			{

				if (S[i] == '.') {
					word = S[i] + str;
					str = "";
				}
				else {

					str += S[i];
				}

				temp = word + temp;
				word = "";

			}

			if (str != "") {
				temp = str + temp;
			}

			return temp;

		}
	};


