	class Solution {
	public:
		long long findMinDiff(vector<long long> arr, long long n, long long m) {
			//code

			sort(arr.begin(), arr.end());

			if (m > n) {
				return -1;
			}

			int ans = INT_MAX;

			for (int i = 0 ;  i + m - 1 < n; i++) {

				int x = arr[i + m - 1] - arr[i];

				ans = min(ans, x);

			}

			return ans;

		}
	};




